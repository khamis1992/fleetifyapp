// Function to get the Arabic translation of an account name
export const getAccountNameTranslation = (accountName: string): string => {
  // Direct translation lookup
  if (accountNamesTranslation[accountName]) {
    return accountNamesTranslation[accountName];
  }

  // Try to find partial matches for compound names
  for (const [english, arabic] of Object.entries(accountNamesTranslation)) {
    if (accountName.includes(english)) {
      return accountName.replace(english, arabic);
    }
  }

  // If no translation found, return the original name
  return accountName;
};